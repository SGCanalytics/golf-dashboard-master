# ------------------------------------------------------------
# DISTANCE BUCKETS
# ------------------------------------------------------------
def bucket_distance(dist):
    if dist < 50:
        return "<50"
    if dist < 100:
        return "50–100"
    if dist < 150:
        return "100–150"
    if dist < 200:
        return "150–200"
    return "200+"

# ------------------------------------------------------------
# SAFE DIVIDE
# ------------------------------------------------------------
def safe_divide(a, b):
    return a / b if b else 0
